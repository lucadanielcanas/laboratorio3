package Ejercicio1;

public class Ejercicio1 {
    public static void main(String[] args)
    {
        int N = 10;
        double A = 20.08;
        char C = 'L';

        double suma = N + A;
        double resto = A - N;
        int numericoDeC = (int)C;

        System.out.println("El valor de N es: " + N + "\n");
        System.out.println("El valor de A es: " + A + "\n");
        System.out.println("El valor de C es: " + C + "\n");
        System.out.println("El valor de la suma entre N y A es: " + suma + "\n");
        System.out.println("El valor de la resta entre N y A es: " + resto + "\n");
        System.out.println("El valor numerico de C (L) es: " + numericoDeC + "\n");
    }
}
